const searchInput = document.getElementById("search");
const jobContainer = document.getElementById("jobContainer");

// ********************* Function To Display *********************** //

function displayJobs(filteredJobs) {
  jobContainer.innerHTML = ""; // Clear previous results
  filteredJobs.forEach((job) => {
    const jobCard = document.createElement("div");
    jobCard.classList.add("job-card");
    jobCard.innerHTML = ` <img src ="${job.img}" class="company-img"/>
    <h3>${job.title}</h3>
    <p>${job.description}</p> 
    <button id="searchLink" data-link="${job.links}">Apply</button>
     <button>Message</button>`;
    jobContainer.appendChild(jobCard);
  });
}

displayJobs(job);

// ***************************************************** //

searchInput.addEventListener("input", () => {
  const searchText = searchInput.value.toLowerCase();
  const filteredJobs = job.filter((job) =>
    job.title.toLowerCase().includes(searchText)
  );
  displayJobs(filteredJobs);
});

// ******************************************

document.querySelectorAll("#searchLink").forEach((button) => {
  button.addEventListener("click", (event) => {
    const jobLink = event.target.getAttribute("data-link");
    if (jobLink) {
      window.open(jobLink, "_blank"); // Open job link in new tab
    }
  });
});
